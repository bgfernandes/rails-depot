# frozen_string_literal: true

# Mailer for sending order received and order shipped emails
class OrderMailer < ApplicationMailer
  default from: Rails.application.credentials.email_settings[:email_from]

  # Subject can be set in your I18n file at config/locales/en.yml
  # with the following lookup:
  #
  #   en.order_mailer.received.subject
  #
  def received(order)
    @order = order

    mail to: order.email, subject: 'Cookie Store Order Confirmation'
  end

  # Subject can be set in your I18n file at config/locales/en.yml
  # with the following lookup:
  #
  #   en.order_mailer.shipped.subject
  #
  def shipped(order)
    @order = order

    mail to: order.email, subject: 'Cookie Store Order Shipped'
  end
end
